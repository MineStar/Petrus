package de.minestar.petrus.common;

public class StartPosition {

    private String team;

    private double x;
    private double y;
    private double z;

    protected StartPosition() {
        // For serialization
    }

    public String getTeam() {
        return team;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }

    @Override
    public String toString() {
        return "StartPosition [team=" + team + ", x=" + x + ", y=" + y + ", z=" + z + "]";
    }

}
